int	main(int ac, char **av)
{
	t_philo	philo;

	if (ac < 4 || ac > 5)
		ft_error("Error: input parameters\n");
	ft_handle_input(ac, av, &philo);
	ft_create_thread();
	free(philo.philos);
	free(philo.forks);
	return (0);
}
